package estruturaRepeticao;

public class While {

    public static void main(String[] args) {
        //While
        
        int contador =1;
        while (contador <=10) {
            System.out.println(contador);
            contador++;
            
        }
        
        boolean condicao = true;
        while (condicao) {
            condicao = false;          
        }
    }

}
